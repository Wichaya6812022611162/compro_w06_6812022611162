#include <stdio.h>

int main() {
    // กำหนดค่าเริ่มต้นตัวแปร
    int a = 5;
    int b = 2;
    float x = 3.0;
    float y = 4.5;

    // ตัวแปรสำหรับเก็บผลลัพธ์ของแต่ละนิพจน์
    int r1;
    int r2;
    float r3;
    float r4;

    // คำนวณนิพจน์ที่ 1: int r1 = a++ * b + (int)y % 3;
    // อธิบาย:
    // a++: ค่า a (5) จะถูกใช้ในการคำนวณก่อน แล้วค่อยเพิ่มค่า a เป็น 6
    // (int)y: แปลงค่า y (4.5) เป็น int จะได้ 4
    // (int)y % 3: 4 % 3 ได้ 1
    // a++ * b: 5 * 2 ได้ 10
    // 10 + 1 ได้ 11
    r1 = a++ * b + (int)y % 3;
    printf("นิพจน์ที่ 1 (r1): %d\n", r1);
    printf("  หลังการคำนวณ r1, ค่า a คือ: %d\n\n", a); // แสดงค่า a หลัง a++

    // คำนวณนิพจน์ที่ 2: int r2 = (a > b) && ((int)x / b < 2);
    // อธิบาย:
    // a: ตอนนี้ค่า a คือ 6 (จาก a++ ในนิพจน์ที่แล้ว)
    // (a > b): (6 > 2) เป็นจริง (true)
    // (int)x: แปลงค่า x (3.0) เป็น int จะได้ 3
    // (int)x / b: 3 / 2 ได้ 1 (การหารจำนวนเต็ม)
    // (1 < 2): เป็นจริง (true)
    // true && true: เป็นจริง (true) ซึ่งในภาษา C จะแทนด้วย 1
    r2 = (a > b) && ((int)x / b < 2);
    printf("นิพจน์ที่ 2 (r2): %d\n\n", r2);

    // คำนวณนิพจน์ที่ 3: float r3 = ++x * y - a / 2;
    // อธิบาย:
    // ++x: ค่า x (3.0) จะถูกเพิ่มเป็น 4.0 ก่อนนำไปใช้
    // a: ค่า a คือ 6
    // ++x * y: 4.0 * 4.5 ได้ 18.0
    // a / 2: 6 / 2 ได้ 3 (การหารจำนวนเต็ม)
    // 18.0 - 3 ได้ 15.0
    r3 = ++x * y - a / 2;
    printf("นิพจน์ที่ 3 (r3): %.2f\n", r3); // แสดงผล float 2 ตำแหน่งทศนิยม
    printf("  หลังการคำนวณ r3, ค่า x คือ: %.1f\n\n", x); // แสดงค่า x หลัง ++x

    // คำนวณนิพจน์ที่ 4: float r4 = ((x + 1.5) > y) || (b-- > 0);
    // อธิบาย:
    // x: ตอนนี้ค่า x คือ 4.0 (จาก ++x ในนิพจน์ที่แล้ว)
    // b: ค่า b คือ 2
    // (x + 1.5): 4.0 + 1.5 ได้ 5.5
    // ((x + 1.5) > y): (5.5 > 4.5) เป็นจริง (true)
    // b--: ค่า b (2) จะถูกใช้ในการคำนวณก่อน แล้วค่อยลดค่า b เป็น 1
    // (b-- > 0): (2 > 0) เป็นจริง (true)
    // true || true: เป็นจริง (true) ซึ่งในภาษา C จะแทนด้วย 1.0 (เมื่อเก็บใน float)
    r4 = ((x + 1.5) > y) || (b-- > 0);
    printf("นิพจน์ที่ 4 (r4): %.2f\n", r4); // แสดงผล float 2 ตำแหน่งทศนิยม
    printf("  หลังการคำนวณ r4, ค่า b คือ: %d\n\n", b); // แสดงค่า b หลัง b--

    return 0;
}