#include <stdio.h> // สำหรับ printf

int main() {
    // บรรทัดที่ 1: กำหนดค่าเริ่มต้น
    double x = 1.0; // กำหนดค่าเริ่มต้นให้ x เป็น 1.0 (ทศนิยม)
    double y = 2.0; // กำหนดค่าเริ่มต้นให้ y เป็น 2.0 (ทศนิยม)
    double z;       // ประกาศตัวแปร z แต่ยังไม่ให้ค่าเริ่มต้น

    printf("\n--- w06-02-02 ---\n");
    printf("บรรทัดที่ 1: double x = 1.0, y = 2.0;\n");
    printf("   x = %.1f, y = %.1f, z = (ยังไม่มีค่า)\n", x, y);
    printf("------------------------------\n");

    // บรรทัดที่ 2: x = y + 5.0;
    x = y + 5.0; // y คือ 2.0. x จะมีค่าเท่ากับ 2.0 + 5.0 = 7.0
    printf("บรรทัดที่ 2: x = y + 5.0;\n");
    printf("   x = %.1f, y = %.1f, z = (ยังไม่มีค่า)\n", x, y);
    printf("------------------------------\n");

    // บรรทัดที่ 3: y = x / 2.0;
    y = x / 2.0; // x คือ 7.0. y จะมีค่าเท่ากับ 7.0 / 2.0 = 3.5
    printf("บรรทัดที่ 3: y = x / 2.0;\n");
    printf("   x = %.1f, y = %.1f, z = (ยังไม่มีค่า)\n", x, y);
    printf("------------------------------\n");

    // บรรทัดที่ 4: y = (x * 3.0) + 4.0;
    y = (x * 3.0) + 4.0; // x คือ 7.0. x * 3.0 คือ 7.0 * 3.0 = 21.0
                         // y จะมีค่าเท่ากับ 21.0 + 4.0 = 25.0
    printf("บรรทัดที่ 4: y = (x * 3.0) + 4.0;\n");
    printf("   x = %.1f, y = %.1f, z = (ยังไม่มีค่า)\n", x, y);
    printf("------------------------------\n");

    // บรรทัดที่ 5: x = -0.5 - y;
    x = -0.5 - y; // y คือ 25.0. x จะมีค่าเท่ากับ -0.5 - 25.0 = -25.5
    printf("บรรทัดที่ 5: x = -0.5 - y;\n");
    printf("   x = %.1f, y = %.1f, z = (ยังไม่มีค่า)\n", x, y);
    printf("------------------------------\n");

    // บรรทัดที่ 6: z = x + y;
    z = x + y; // x คือ -25.5, y คือ 25.0. z จะมีค่าเท่ากับ -25.5 + 25.0 = -0.5
    printf("บรรทัดที่ 6: z = x + y;\n");
    printf("   x = %.1f, y = %.1f, z = %.1f\n", x, y, z);
    printf("------------------------------\n");

    return 0;
}