#include <stdio.h> // Header file สำหรับฟังก์ชัน input/output เช่น printf

int main() {
    // กำหนดค่าเริ่มต้นของตัวแปร
    int a = 5;
    int b = 17;
    double c = 8.5;
    double d = 4.0;

    // บรรทัดที่ 1: แสดงค่าเริ่มต้นของตัวแปรทั้งหมด
    printf("บรรทัดที่ 1: ค่าเริ่มต้นของตัวแปร:\n");
    printf("   a = %d\n", a);
    printf("   b = %d\n", b);
    printf("   c = %.1f\n", c);
    printf("   d = %.1f\n", d);
    printf("------------------------------\n");

    // บรรทัดที่ 2: ผลการคำนวณของ d + a
    double result_d_plus_a = d + a;
    printf("บรรทัดที่ 2: ผลการคำนวณของ d + a = %.1f\n", result_d_plus_a);
    printf("------------------------------\n");

    // บรรทัดที่ 3: ผลการคำนวณของ a - b
    int result_a_minus_b = a - b;
    printf("บรรทัดที่ 3: ผลการคำนวณของ a - b = %d\n", result_a_minus_b);
    printf("------------------------------\n");

    // บรรทัดที่ 4: ผลการคำนวณของ c * d
    double result_c_times_d = c * d;
    printf("บรรทัดที่ 4: ผลการคำนวณของ c * d = %.1f\n", result_c_times_d);
    printf("------------------------------\n");

    // บรรทัดที่ 5: ผลการคำนวณของ a * c
    double result_a_times_c = a * c;
    printf("บรรทัดที่ 5: ผลการคำนวณของ a * c = %.1f\n", result_a_times_c);
    printf("------------------------------\n");

    // บรรทัดที่ 6: ผลการคำนวณของ c / d
    double result_c_div_d = c / d;
    printf("บรรทัดที่ 6: ผลการคำนวณของ c / d = %.3f\n", result_c_div_d);
    printf("------------------------------\n");

    // บรรทัดที่ 7: ผลการคำนวณของ b / c
    double result_b_div_c = (double)b / c;
    printf("บรรทัดที่ 7: ผลการคำนวณของ b / c = %.3f\n", result_b_div_c);
    printf("------------------------------\n");

    // บรรทัดที่ 8: ผลการคำนวณของ a % b (Modulo - การหารเอาเศษ)
    int result_a_mod_b = a % b;
    printf("บรรทัดที่ 8: ผลการคำนวณของ a %% b (หารเอาเศษ) = %d\n", result_a_mod_b);
    printf("------------------------------\n");

    // บรรทัดที่ 9: ผลการคำนวณของ c % a (C ไม่รองรับ % กับ float/double โดยตรง)
    printf("บรรทัดที่ 9: (C ไม่รองรับ %% กับค่าทศนิยมโดยตรงสำหรับ c %% a)\n");
    printf("------------------------------\n");

    // บรรทัดที่ 10: ผลการคำนวณของ c % d (C ไม่รองรับ % กับ float/double โดยตรง)
    printf("บรรทัดที่ 10: (C ไม่รองรับ %% กับค่าทศนิยมโดยตรงสำหรับ c %% d)\n");
    printf("------------------------------\n");

    return 0;
}